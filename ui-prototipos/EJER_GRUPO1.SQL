CREATE TABLE CLIENTES(
CED_CLI VARCHAR(10) PRIMARY KEY,
NOM_CLI VARCHAR(30) NOT NULL,
APE_CLI VARCHAR(30) NOT NULL,
TEL_CLI VARCHAR(10) NOT NULL,
DIR_CLI VARCHAR(30) NOT NULL
);

CREATE TABLE EMPLEADOS(
CED_EMP VARCHAR(10) PRIMARY KEY,
NOM_EMP VARCHAR(30) NOT NULL,
APE_EMP VARCHAR(30) NOT NULL,
DIR_EMP VARCHAR(30) NOT NULL
);

CREATE TABLE PRODUCTOS(
COD_PRO VARCHAR(10) PRIMARY KEY,
NOM_PRO VARCHAR(20) NOT NULL,
MAR_PRO VARCHAR(20) NOT NULL,
PRE_UNI NUMBER(10,2) NOT NULL,
FECHA_CAD DATE NOT NULL,
STOCK NUMBER(10) NOT NULL
);

CREATE TABLE FACTURA(
NUM_FAC NUMBER(10) PRIMARY KEY,
FEC_FAC DATE NOT NULL,
CLI_PER VARCHAR(10) NOT NULL,
EMP_PER VARCHAR(10) NOT NULL,
ESTADO VARCHAR(20) NOT NULL,
TOTAL NUMBER(10,2) NOT NULL,
FOREIGN KEY (CLI_PER) REFERENCES CLIENTES(CED_CLI),
FOREIGN KEY(EMP_PER) REFERENCES EMPLEADOS(CED_EMP)
);

CREATE TABLE DETALLE_VENTA(
ID_DET NUMBER(10) PRIMARY KEY,
COD_PRO_V VARCHAR(10)NOT NULL,
CANTIDAD NUMBER(10) NOT NULL,
NUM_FAC_PER NUMBER(10)NOT NULL,
FOREIGN KEY(COD_PRO_V) REFERENCES PRODUCTOS(COD_PRO),
FOREIGN KEY(NUM_FAC_PER) REFERENCES FACTURA(NUM_FAC)
);
